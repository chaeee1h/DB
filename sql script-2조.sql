CREATE TABLE MILEAGE(
   OrderNumber   Int               NOT NULL,
   AccDate   Date            NOT NULL,
   ExpDate Date            NOT NULL,
   AccRate      VarChar(30)         NOT NULL,
   AccAmount      Int               NOT NULL,
   
   CONSTRAINT   MILEAGE_PK      PRIMARY KEY(OrderNumber),
   CONSTRAINT  MILEAGE_AccRate
               CHECK(AccRate IN('1%','1.5%','2%')),
   CONSTRAINT   MILEAGE_ORDER_FK   FOREIGN KEY(OrderNumber)
                  REFERENCES QUOTED_ID(OrderNumber)
                  ON UPDATE Cascade
                  ON DELETE Cascade
);

CREATE TABLE MILEAGE_EVENT(
   MemberID      Int               NOT NULL,
   EventNum      Char(30)         NOT NULL,
   EventType      VarChar(30)         NOT NULL,
   EventStart   Date            NOT NULL,
   EventEnd    Date            NOT NULL,
   CONSTRAINT      MILEAGE_EVENT_PK   PRIMARY KEY(MemberID, EventNum),
